def Task24():
    print("Задача 24: В фермерском хозяйстве в Карелии выращивают чернику. Она растёт на круглой грядке, причём кусты высажены только по окружности. Таким образом, у каждого куста есть ровно два соседних. Всего на грядке растёт N кустов.\nЭти кусты обладают разной урожайностью, поэтому ко времени сбора на них выросло различное число ягод — на i-ом кусте выросло ai ягод.\nВ этом фермерском хозяйстве внедрена система автоматического сбора черники. Эта система состоит из управляющего модуля и нескольких собирающих модулей. Собирающий модуль за один заход, находясь непосредственно перед некоторым кустом, собирает ягоды с этого куста и с двух соседних с ним.\nНапишите программу для нахождения максимального числа ягод, которое может собрать за один заход собирающий модуль, находясь перед некоторым кустом заданной во входном файле грядки.\n\tПример\n\t4 -> 1 2 3 4\n\t9")

    n = int(input("Введите кол_во кустов с ягодами: "))
    list_1 = list(map(int, input().split(",")))[:n]
    print(list_1)
    x = int(input("цифра в массиве: "))
    for i in range(0, len(list_1)):
        if x == list_1[i]:
            summa = x + list_1[i - 1] + list_1[i + 1]
    print(summa)
