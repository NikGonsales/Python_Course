def Task22():
    print("Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с повторениями). Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах. \nПользователь вводит 2 числа. n — кол-во элементов первого множества. m — кол-во элементов второго множества. Затем пользователь вводит сами элементы множеств.\nПример\n\t11 6\n\t2 4 6 8 10 12 10 8 6 4 2\n\t3 6 9 12 15 18\n\t6 12")

    n = int(input("Введите кол-во элементов первого множества: "))
    m = int(input("Введите кол-во элементов второго множества: "))
    import random
    list_1 = set(random.randint(1, 20) for i in range(n))
    list_2 = set(random.randint(1, 20) for i in range(m))
    print( *list_1,"\n",*list_2)
    list_3 = sorted((list_1 & list_2))
    print(*list_3)
